package com.shashank.platform.classroomappui.models;

public class Exam {


    private String title;
    private String code;
    private String start;
    private String indexL;
    private String end;
    private String credit;
    private String venue;
    private String invigilators;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getIndexL() {
        return indexL;
    }

    public void setIndexL(String indexL) {
        this.indexL = indexL;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getVenue() {
        return venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public String getInvigilators() {
        return invigilators;
    }

    public void setInvigilators(String invigilators) {
        this.invigilators = invigilators;
    }
}
